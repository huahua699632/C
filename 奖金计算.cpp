#include <cstdio>
using namespace std;

double i, ans;

int main()
{
	scanf("%lf", &i);
	if(i && (i <= 100000))
		ans = 0.1 * i;
	if((i > 100000) && (i <= 200000))
		ans = 0.1 * 100000 + (i - 100000) * 0.075;
	if((i > 200000) && (i <= 400000))
		ans = 0.1 * 100000 + 0.075 * 100000 + (i - 200000) * 0.05;
	if((i > 400000) && (i <= 600000))
		ans = 0.1 * 100000 + 0.075 * 100000 + 0.05 * 200000 + (i - 400000) * 0.03;
	if((i > 600000) && (i <= 1000000))
		ans = 0.1 * 100000 + 0.075 * 100000 + 0.05 * 200000 + 0.03 * 200000 + (i - 600000) * 0.015;
	if(i > 1000000)
		ans = 0.1 * 100000 + 0.075 * 100000 + 0.05 * 200000 + 0.03 * 200000 + 0.015 * 400000 + (i - 1000000) * 0.01;
	printf("%.2f\n", ans);
		 
} 
